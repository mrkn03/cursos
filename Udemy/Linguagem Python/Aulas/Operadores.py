#OPERADORES ARITMÉTICOS
x = 5
y = 2

z = 5 + 2   #ADIÇAO
print(z)
z = 5 - 2   #SUBTRAÇÃO
print(z)
z = 5 * 2   #MULTIPLICAÇÃO
print(z)
z = 5 / 2   #DIVISÃO
print(z)
z = 5 % 2   #RESTO DA DIVISÃO ("MOD")
print(z)
z = 5 ** 2  #EXPONENCIAÇÃO
print(z)
z = 5 // 2  #DIVISÃO INTEIRA
print(z)

#OPERADORES COMPARATIVOS
z = 5 < 2   #5 MENOR QUE 2
print(z)
z = 5 > 2   #5 MAIOR QUE 2
print(z)
z = 5 <= 2  #5 MENOR OU IGUAL A 2
print(z)
z = 5 >= 2  #5 MAIOR OU IGUAL A 2
print(z)
z = 5 == 2  #5 IGUAL A
print(z)
z = 5 != 2  #5 DIFERENTE DE 2
print(z)

#OPERADORES LÓGICOS
z = 5 and 2 #5 E 2
print(z)
z = 5 or 2  #5 OU 2 
print(z)
#z = 5 not 2 #5 NAO 2
print(z)

